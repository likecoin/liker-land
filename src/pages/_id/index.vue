<template>
  <Page>

    <div
      :class="[
        'flex',

        'flex-col',
        'desktop:flex-row',

        'items-center',
        'desktop:items-start',
        'desktop:justify-center',

        'mt-[32px]',
      ]"
    >
      <div
        :class="[
          'mb-[24px]',
          'desktop:mr-[24px]',

          'w-full',
          'desktop:w-[280px]',
        ]"
      >
        <NFTPortfolioUserInfo
          :user-info="userInfo"
          :wallet="wallet"
        >
          <div class="flex justify-between w-[44px] mx-auto mt-[16px] mb-[24px] text-shade-gray">
            <IconEllipse />
            <IconEllipse />
            <IconEllipse />
          </div>
          <UserStatsPortfolio
            class="grid grid-cols-2 cursor-default gap-x-8 gap-y-4 text-medium-gray"
            :collected-items="ownedNFTs"
            :created-class-ids="sellingNFTClassIds"
            :is-loading="isLoading"
          />
        </NFTPortfolioUserInfo>
        <div class="flex justify-center mt-[18px]">
          <ButtonV2
            v-if="getAddress === wallet"
            preset="outline"
            text="My Dashboard"
            @click="goMyDashboard"
          >
            <template #prepend>
              <IconPerson />
            </template>
          </ButtonV2>
        </div>
      </div>
      <div
        :class="[
          'flex',
          'flex-col',
          'items-center',
          'w-full',
          'max-w-[636px]',
          'desktop:w-[636px]',
        ]"
      >
        <div :class="['flex','items-center','mb-[48px]','w-full']">
          <div
            :class="[
              'flex',
              'justify-center',
              'items-center',
              'p-[4px]',
              'mx-auto',
              'bg-shade-gray',
              'rounded-[14px]',
            ]"
          >
            <MenuButton
              text="Collected"
              :is-selected="currentTab === 'collected'"
              @click="handleGoCollected"
            />
            <MenuButtonDivider v-if="sellingNFTClassIds.length" />
            <MenuButton
              v-if="isLoading || sellingNFTClassIds.length"
              text="Created"
              :is-selected="currentTab === 'created'"
              @click="handleGoCreated"
            />
          </div>
          <ShareButton @copy="handleCopyURL" />
        </div>

        <CardV2 v-if="isLoading">Loading</CardV2>
        <div v-else>
          <ul
            v-if="currentTab === 'collected'"
            :class="[
              'w-full',
              'mx-auto',

              'columns-1',
              'laptop:columns-2',

              'gap-[16px]',
            ]"
          >
            <li>
              <NFTPortfolioEmpty v-if="!ownedNFTs.length" preset="collected" />
            </li>
            <li
              v-for="id in ownedNFTClassIds"
              :key="id"
            >
              <NFTPortfolioItem
                :class-id="id"
              />
            </li>
          </ul>

          <ul
            v-if="currentTab === 'created'"
            :class="[
              'w-full',
              'mx-auto',

              'columns-1',
              'laptop:columns-2',

              'gap-[16px]',
            ]"
          >
            <li>
              <NFTPortfolioEmpty v-if="!sellingNFTClassIds.length" preset="created" />
            </li>
            <li
              v-for="id in sellingNFTClassIds"
              :key="id"
            >
              <NFTPortfolioItem
                :class-id="id"
              />
            </li>
          </ul>
          <div class="flex flex-col items-center my-[48px] w-full">
            <div class="w-[32px] h-[2px] bg-shade-gray mb-[32px]" />
            <ButtonV2
              preset="outline"
              :text="$t('portfolio_finding_more_button')"
              to="/campaign/writing-nft"
            />
          </div>
        </div>
      </div>
    </div>
  </Page>
</template>

<script>
import { getUserMinAPI, getAddressLikerIdMinApi } from '~/util/api';
import { convertAddressPrefix, isValidAddress } from '~/util/cosmos';
import { ellipsis } from '~/util/ui';
import { checkUserNameValid } from '~/util/user';
import { logTrackerEvent } from '~/util/EventLogger';

import walletMixin from '~/mixins/wallet';
import portfolioMixin from '~/mixins/portfolio';

export default {
  name: 'NFTPortfolioPage',
  layout: 'default',
  filters: {
    ellipsis,
  },
  mixins: [walletMixin, portfolioMixin],
  head() {
    const name = ellipsis(this.userDisplayName);
    const title = this.$t('portfolio_title', { name });
    const description = this.$t('portfolio_description', { name });
    return {
      title,
      meta: [
        {
          hid: 'og:title',
          property: 'og:title',
          content: title,
        },
        {
          hid: 'description',
          name: 'description',
          content: description,
        },
        {
          hid: 'og:description',
          property: 'og:description',
          content: description,
        },
        {
          hid: 'og:image',
          property: 'og:image',
          content:
            this.userAvatar ||
            `https://avatars.dicebear.com/api/identicon/${this.wallet}/600.png`,
        },
      ],
    };
  },
  data() {
    return {
      wallet: undefined,
      userInfo: null,
    };
  },
  computed: {
    userDisplayName() {
      return (this.userInfo && this.userInfo.displayName) || this.wallet;
    },
  },
  async asyncData({ route, $api, error }) {
    let { id } = route.params;
    if (id && isValidAddress(id)) {
      if (id.startsWith('cosmos1')) {
        id = convertAddressPrefix(id, 'like');
      }
      if (id.startsWith('like1')) {
        try {
          const userInfo = await $api.get(getAddressLikerIdMinApi(id), {
            validateStatus: code => code < 500 && code !== 400,
          });
          return {
            userInfo: userInfo.data,
            wallet: id,
          };
        } catch (error) {
          // eslint-disable-next-line no-console
          console.error(error);
        }
        return {
          wallet: id,
        };
      }
    }
    if (id && checkUserNameValid(id)) {
      try {
        const userInfo = await $api.$get(getUserMinAPI(id));
        return {
          userInfo,
          wallet: userInfo.likeWallet,
        };
      } catch (err) {
        const msg = (err.response && err.response.data) || err;
        // eslint-disable-next-line no-console
        console.error(msg);
      }
    }
    error({ statusCode: 404, message: 'LIKER_NOT_FOUND' });
    return undefined;
  },
  mounted() {
    this.fetchUserSellingClasses(this.wallet);
    this.fetchUserCollectedNFTs(this.wallet);
  },
  methods: {
    handleGoCollected() {
      logTrackerEvent(this, 'UserPortfolio', 'GoCollectedTab', this.wallet, 1);
      this.goCollected();
    },
    handleGoCreated() {
      logTrackerEvent(this, 'UserPortfolio', 'GoCreatedTab', this.wallet, 1);
      this.goCreated();
    },
    goMyDashboard() {
      logTrackerEvent(this, 'UserPortfolio', 'GoToMyDashboard', this.wallet, 1);
      this.$router.push({
        name: 'dashboard',
      });
    },
    handleCopyURL() {
      this.copyURL(this.wallet);
      logTrackerEvent(this, 'UserPortfolio', 'CopyShareURL', this.wallet, 1);
    },
  },
};
</script>
